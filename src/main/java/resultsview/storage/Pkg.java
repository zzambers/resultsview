package resultsview.storage;

/**
 *
 * @author zzambers
 */
public class Pkg {

    String strId;

    public Pkg(String strId) {
        this.strId = strId;
    }

    public String getStrId() {
        return strId;
    }

    public void setStrId(String strId) {
        this.strId = strId;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (getClass() != o.getClass()) {
            return false;
        }
        Pkg o1 = (Pkg) o;
        if (!strId.equals(o1.strId)) {
            return false;
        }
        return true;
    }

    @Override
    public int hashCode() {
        return strId.hashCode();
    }

}
